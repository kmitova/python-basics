number = int(input())
if 0 <= number <= 9:
    if number == 0:
        print("zero")
    elif number == 1:
        print("one")
    elif number == 2:
        print("two")
    elif number == 3:
        print("three")
    elif number == 4:
        print("four")
    elif number == 5:
        print("five")
    elif number == 6:
        print("six")
    elif number == 7:
        print("seven")
    elif number == 8:
        print("eight")
    else:
        print("nine")
else:
    print("number too big")
